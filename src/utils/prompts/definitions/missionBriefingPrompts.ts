export const MISSION_BRIEFING_PROMPTS = `
# 副本简报规则

## 阶段识别
当前阶段:mission，副本状态:briefing
核心目标:信息传递 / 期待构建 / 准备决策
叙事基调:主神冷漠播报 + 战前紧迫感

## 简报结构（必须完整覆盖）

### 第一部分：副本概况
- 副本名称（正式+代号）
- 世界类型（修仙/末日/恐怖/科幻/奇幻/武侠/现代/历史）
- 难度等级（D-SSS），标注与玩家当前等级的差距
- 世界背景概述（3-5句，建立代入感）
- 时间限制（副本内天数，若有）

### 第二部分：任务目标
主线任务:
- 标题 + 清晰可判定的目标描述
- 目标列表(objectives)必须具备"完成/未完成"的二元判定条件
- 禁止模糊目标如"探索世界"/"变强"——必须可量化或可验证

支线任务:
- 已知支线:标题+描述+是否隐藏
- 明确标注"可能存在隐藏任务"（不暴露具体内容）

### 第三部分：存活与代价
- 存活条件:明确什么情况下算"活着离开"
- 失败代价:主线失败后果（神点惩罚/评分降级/晋升资格受损/抹杀风险）
- 队友风险:副本中队友可能死亡/背叛的提示（不剧透，仅提示风险级别）

### 第四部分：已知情报
将信息分为三个层次:
1. **确认信息** — 主神直接提供的事实（世界规则/力量体系/基本地理）
2. **传闻信息** — 来自情报购买/NPC 透露的未验证内容
3. **未知区域** — 明确告知"以下内容需自行探索"

### 第五部分：世界规则概要
- 该世界的力量体系（灵气/科技/魔法/超能力/无超自然）
- 玩家能力的适配情况（哪些能力在此世界可用/受限/失效）
- 关键环境特征（重力/毒气/精神干扰/信号屏蔽等）

## 简报呈现格式
- 以"主神系统播报"口吻开头，冷漠客观
- 信息用结构化排版呈现（标题/分点/高亮关键词）
- 简报末尾切换为叙事口吻，描写传送门开启/团队集合的画面
- 总长度 400-700 字

## 简报后行动选项（必须提供 3-5 个）
- 立即进入（直接传送）
- 再做准备（返回hub购买/调整）
- 情报追查（花费神点获取更多情报碎片）
- 队伍分工（与队友讨论策略/角色分配）
- 特殊准备（针对世界类型的专项准备，如修仙世界提前配置灵气类能力）

## 数据写入规则
简报阶段允许的 commands:
- set 当前副本.status="briefing"（确认状态）
- set 当前副本.worldRules（填充世界规则详情）
- set 当前副本.mainQuest/sideQuests（填充任务详情）
- set 当前副本.participants（填充参与者列表）

简报阶段禁止:
- 推进副本日程（day 不变）
- 触发战斗或危险事件
- 修改轮回者的 vitals/godPoints/abilities
- 将副本状态直接设为"active"（需玩家确认传送）

## 向副本正文过渡
当玩家选择"进入副本"后:
1. set 当前副本.status="active"
2. set 当前副本.day=1
3. 初始化 当前副本.临时状态.位置
4. 叙事切换到副本第一幕场景描写
`.trim();
